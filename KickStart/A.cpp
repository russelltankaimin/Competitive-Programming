void solve(){
  int length;
  int score;
  string text;
  cin >> length >> score >> text;
  int currScore = 0;
  for (int i = 0; i < length / 2; i++) {
    currScore += (text[i] != text[length - i - 1]);
  }
  cout << abs(score - currScore) << endl;
}

int main(){
  int T;
  cin >> T;
  for (int i = 1; i <= T; i++) {
    cout << "Case #" << i << ": ";
    solve();
  }
  return 0;
}
